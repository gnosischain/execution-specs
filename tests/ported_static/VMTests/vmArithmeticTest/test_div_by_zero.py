@pytest.mark.pre_alloc_mutable
def test_div_by_zero(
    state_test: StateTestFiller,
    pre: Alloc,
    tx_data_hex: str,
    expected_post: dict,
) -> None:
    """Ori Pomerantz qbzzt1@gmail.com."""
    coinbase = Address("0x2adc25665018aa1fe0e6bc666dac8fc2697ff9ba")
    sender = EOA(
        key=0x40AC0FC28C27E961EE46EC43355A094DE205856EDBD4654CF2577C2608D4EC1E
    )

    env = Environment(
        fee_recipient=coinbase,
        number=1,
        timestamp=1000,
        prev_randao=0x20000,
        base_fee_per_gas=10,
        gas_limit=100000000,
    )

    pre[sender] = Account(balance=0xBA1A9CE0BA1A9CE)
    # Source: LLL
    # {
    #     (def 'NOP 0)
    #     (def 'opcode $4)
    #     (def 'a      $36)
    #     (def 'b      $68)
    #
    #     (if (= opcode 0x04) [[0]] (div a 0) NOP)
    #     (if (= opcode 0x05) [[0]] (sdiv a 0) NOP)
    #     (if (= opcode 0x06) [[0]] (mod a 0) NOP)
    #     (if (= opcode 0x07) [[0]] (smod a 0) NOP)
    #     (if (= opcode 0x08) [[0]] (addmod a b 0) NOP)
    #     (if (= opcode 0x09) [[0]] (mulmod a b 0) NOP)
    # }
    contract = pre.deploy_contract(
        code=(
            Op.JUMPI(pc=0xF, condition=Op.EQ(Op.CALLDATALOAD(offset=0x4), 0x4))
            + Op.POP(0x0)
            + Op.JUMP(pc=0x19)
            + Op.JUMPDEST
            + Op.SSTORE(
                key=0x0, value=Op.DIV(Op.CALLDATALOAD(offset=0x24), 0x0)
            )
            + Op.JUMPDEST
            + Op.JUMPI(
                pc=0x29, condition=Op.EQ(Op.CALLDATALOAD(offset=0x4), 0x5)
            )
            + Op.POP(0x0)
            + Op.JUMP(pc=0x33)
            + Op.JUMPDEST
            + Op.SSTORE(
                key=0x0, value=Op.SDIV(Op.CALLDATALOAD(offset=0x24), 0x0)
            )
            + Op.JUMPDEST
            + Op.JUMPI(
                pc=0x43, condition=Op.EQ(Op.CALLDATALOAD(offset=0x4), 0x6)
            )
            + Op.POP(0x0)
            + Op.JUMP(pc=0x4D)
            + Op.JUMPDEST
            + Op.SSTORE(
                key=0x0, value=Op.MOD(Op.CALLDATALOAD(offset=0x24), 0x0)
            )
            + Op.JUMPDEST
            + Op.JUMPI(
                pc=0x5D, condition=Op.EQ(Op.CALLDATALOAD(offset=0x4), 0x7)
            )
            + Op.POP(0x0)
            + Op.JUMP(pc=0x67)
            + Op.JUMPDEST
            + Op.SSTORE(
                key=0x0, value=Op.SMOD(Op.CALLDATALOAD(offset=0x24), 0x0)
            )
            + Op.JUMPDEST
            + Op.JUMPI(
                pc=0x77, condition=Op.EQ(Op.CALLDATALOAD(offset=0x4), 0x8)
            )
            + Op.POP(0x0)
            + Op.JUMP(pc=0x84)
            + Op.JUMPDEST
            + Op.SSTORE(
                key=0x0,
                value=Op.ADDMOD(
                    Op.CALLDATALOAD(offset=0x24),
                    Op.CALLDATALOAD(offset=0x44),
                    0x0,
                ),
            )
            + Op.JUMPDEST
            + Op.JUMPI(
                pc=0x94, condition=Op.EQ(Op.CALLDATALOAD(offset=0x4), 0x9)
            )
            + Op.POP(0x0)
            + Op.JUMP(pc=0xA1)
            + Op.JUMPDEST
            + Op.SSTORE(
                key=0x0,
                value=Op.MULMOD(
                    Op.CALLDATALOAD(offset=0x24),
                    Op.CALLDATALOAD(offset=0x44),
                    0x0,
                ),
            )
            + Op.JUMPDEST
            + Op.STOP
        ),
        balance=0xBA1A9CE0BA1A9CE,
        nonce=0,
        address=Address("0x935e36ee6cd0f1602d1abca9a391c696cfa4c04d"),  # noqa: E501
    )

    tx_data = bytes.fromhex(tx_data_hex) if tx_data_hex else b""

    tx = Transaction(
        sender=sender,
        to=contract,
        data=tx_data,
        gas_limit=16777216,
        value=1,
    )

    post = expected_post

    state_test(env=env, pre=pre, post=post, tx=tx)
